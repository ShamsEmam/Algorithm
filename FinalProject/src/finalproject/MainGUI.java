    public static void quickSort(ArrayList<Patient> arr, int low, int high) {
        if (low < high) {
            int pi = partition(arr, low, high);
            quickSort(arr, low, pi - 1);
            quickSort(arr, pi + 1, high);
        }
    }

    public static int partition(ArrayList<Patient> arr, int low, int high) {
        String pivot = arr.get(high).Id;
        int i = low - 1;
        for (int j = low; j < high; j++) {
            if (arr.get(j).Id.compareTo(pivot) < 0) {
                i++;
                Patient temp = arr.get(i);
                arr.set(i, arr.get(j));
                arr.set(j, temp);
            }
        }
        Patient temp = arr.get(i + 1);
        arr.set(i + 1, arr.get(high));
        arr.set(high, temp);

        return i + 1;
    }

    public static int binarySearchH(ArrayList<Hospital> arr, String target) {
        int left = 0, right = arr.size() - 1;
        while (left <= right) {
            int mid = left + (right - left) / 2;

            // Check if target is present at mid
            if (arr.get(mid).Name.compareTo(target) == 0) {
                return mid;
            }

            // If target greater, ignore left half
            if (arr.get(mid).Name.compareTo(target) < 0) {
                left = mid + 1;
            } else { // If target is smaller, ignore right half
                right = mid - 1;
            }
        }
        // If we reach here, then the element was not present
        return -1;
    }

