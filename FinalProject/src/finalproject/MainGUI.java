            String name = item.Name;
            String tele = item.Tele;
            int age = item.Age;

            Object ptsData[] = {id, name, tele, age};

            DefaultTableModel ptsTemp = (DefaultTableModel) Patient_Table.getModel();

            ptsTemp.addRow(ptsData);
