    private void View_HospitalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_HospitalesActionPerformed
        // TODO add your handling code here:
        jTabbedPane1.setSelectedIndex(4);
        // sort first;

        int n = hospitals.size();
        boolean swapped;
        for (int j = 0; j < n; j++) {
            swapped = false;
            for (int i = 1; i < n; i++) {
                if (hospitals.get(i - 1).Name.compareTo(hospitals.get(i).Name) > 0) {
                    // Swap elements
                    Hospital temp = hospitals.get(i - 1);
                    hospitals.set(i - 1, hospitals.get(i));
                    hospitals.set(i, temp);
                    swapped = true;
                }
            }
            if (!swapped) {
                break;
            }
        }
        //Refresh BooksTable  
        DefaultTableModel Hosts = (DefaultTableModel) Hosts_Table.getModel();
        Hosts.setRowCount(0);

        for (Hospital item : hospitals) {
            String Name = item.Name;
            String Loc = item.Location;

            //String array to store BooksTable Data into jtable
            String HostData[] = {Name, Loc};
            DefaultTableModel HostTemp = (DefaultTableModel) Hosts_Table.getModel();

            //add String array BooksData into JTable
            HostTemp.addRow(HostData);
        }
    }//GEN-LAST:event_View_HospitalesActionPerformed

